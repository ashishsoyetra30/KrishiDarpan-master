<li><span id="s1"><info> 1.Indoor Vertical Farming:</info></span>
 <span id="s2">Indoor vertical farming can increase crop yields, overcome limited land area, and even reduce farming’s impact on the environment by cutting down distance traveled in the supply chain. Indoor vertical farming can be defined as the practice of growing produce stacked one above another in a closed and controlled environment. By using growing shelves mounted vertically, it significantly reduces the amount of land space needed to grow plants compared to traditional farming methods. This type of growing is often associated with city and urban farming because of its ability to thrive in limited space.</span></li><br> 

<li><span id="s1"><info> 2.Farm Automation:</info></span>
 <span id="s2">Farm automation, often associated with “smart farming”, is technology that makes farms more efficient and automates the crop or livestock production cycle. An increasing number of companies are working on robotics innovation to develop drones, autonomous tractors, robotic harvesters, automatic watering, and seeding robots. Although these technologies are fairly new, the industry has seen an increasing number of traditional agriculture companies adopt farm automation into their processes. 

New advancements in technologies ranging from robotics and drones to computer vision software have completely transformed modern agriculture. The primary goal of farm automation technology is to cover easier, mundane tasks. Some major technologies that are most commonly being utilized by farms include: harvest automation, autonomous tractors, seeding and weeding, and drones.</span></li><br>

<li><span id="s1"><info> 3.Modern Greenhouses:</info></span>
  <span id="s2">In recent decades, the Greenhouse industry has been transforming from small scale facilities used primarily for research and aesthetic purposes (i.e., botanic gardens) to significantly more large-scale facilities that compete directly with land-based conventional food production.As the market has grown dramatically, it has also experienced clear trends in recent years. Modern greenhouses are becoming increasingly tech-heavy, using LED lights and automated control systems to perfectly tailor the growing environment. Successful greenhouse companies are scaling significantly and located their growing facilities near urban hubs to capitalize on the ever-increasing demand for local food, no matter the season. </span></li><br>

<li><span id="s1"><info> 4.Precision Agriculture:</info></span>
